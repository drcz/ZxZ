/// THE GAME MAIN DRIVER THINGS ///////////////////////////////////////////////////

void start() {
    zero_ocupied();
    gamestate = TITLE;
    subframe = 0;
    tune_pc = 0;
}

void update () {    
    switch(gamestate) {
        case TITLE:
            dreamy_palette();
            title_frame();
            if (clock>MIN_TIME && (*GAMEPAD1 & (BUTTON_1|BUTTON_2))) {
                gamestate=BRIEFING;
                clock = 0;
            }
            break;

        case BRIEFING:
            briefing_frame();
            if (clock>MIN_TIME && (*GAMEPAD1 & (BUTTON_1|BUTTON_2))) new_game();
            break;
            
        case GAME:
            update_joystick();
            if(subframe==0) update_world();
            if(subframe==7 && music_on) {
                if(tune[tune_pc]<=C2) tone(tune[tune_pc], 40, 30, TONE_PULSE1);
                tone(tune[tune_pc]*2, 20, 30, TONE_PULSE2);
                tune_pc++; tune_pc%=LEN;
            }
            if(subframe==7) reset_joystick(); /// sometimes it's better this way...
            display(subframe);
            break;
            
        case OVER:
            gameover_frame();
            if (clock>MIN_TIME && (*GAMEPAD1 & (BUTTON_1|BUTTON_2))) new_game();            
            break;
            
        case VICTOLY:
            victoly_frame();
            break;
    }    
    subframe++; subframe%=8;
    if(subframe==0) clock++;
}

