int8_t flip_dir(uint8_t dir) {
    switch(dir) {
        case DIR_U: return DIR_D;
        case DIR_D: return DIR_U;
        case DIR_L: return DIR_R;
        case DIR_R: return DIR_L;
    }
    return dir;
}

int8_t flip_v_dir(uint8_t dir) {
    switch(dir) {
        case DIR_U: return DIR_D;
        case DIR_D: return DIR_U;
    }
    return dir;
}



